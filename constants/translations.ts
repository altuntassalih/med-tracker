export type LanguageCode = 'tr' | 'en';

export const t = (lang: LanguageCode, path: string) => {
  const keys = path.split('.');
  let current: any = translations[lang];
  for (const key of keys) {
    if (current[key] === undefined) return path;
    current = current[key];
  }
  return current;
};
